const languageSelect = document.querySelector('.language');

function updateContent(language) {
    document.getElementById('game-title').innerText = translations[language].greeting;
    document.getElementById('download-title').innerText = translations[language].download;
    document.getElementById('mac-btn-text').innerHTML = translations[language].downloadMac + ' <i class="fa-brands fa-apple"></i>';
    document.getElementById('win-btn-text').innerHTML = translations[language].downloadWindows + ' <i class="fa-brands fa-windows"></i>';
    document.getElementById('description-title').innerText = translations[language].descriptionTitle;
    document.getElementById('description-text').innerHTML = translations[language].descriptionText.split('. ').join('<br><br>');
    document.getElementById('gameplay-images-title').innerText = translations[language].gameplayImagesTitle;
}

languageSelect.addEventListener('change', (event) => {
    updateContent(event.target.value);
});

updateContent('en');